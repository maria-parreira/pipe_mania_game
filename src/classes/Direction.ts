
/**
 * Enum representing the Pipe directions.
 * 
 * @enum {string}
 * @property {string} Left - Represents the left direction.
 * @property {string} Right - Represents the right direction.
 * @property {string} Up - Represents the upward direction.
 * @property {string} Down - Represents the downward direction.
 */

export enum Direction{
    Left = "left",
    Right = "right",
    Up = "up",
    Down = "down"
}